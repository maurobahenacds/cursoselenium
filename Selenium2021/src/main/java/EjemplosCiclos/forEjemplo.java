package EjemplosCiclos;

import java.util.Iterator;

public class forEjemplo {

	public static void main(String[] args) {
		int y=10;
		//bucle for comienza cuando x=0
		//'x' es una variable local dentro del 'for'
		//'for' va a correr hasta que y<=5
		
		for (int x=5;x<=y;x++) {
			System.out.println("Valor de x: " +x);
			
		}

	}

}
